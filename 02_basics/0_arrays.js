// map(): Array ke har item par ek function apply karta hai aur ek naya array return karta hai.
// filter(): Ek condition ke anurup array ke items ko filter karta hai aur ek naya array return karta hai.
// reduce(): Array ke items ko ek single value me reduce karta hai, jaise ki total sum ya concatenation.
// forEach(): Har item par ek function ko call karta hai, lekin koi naya array nahi banata hai.
// find(): Ek condition ke anurup pehla matching item ko return karta hai.
// some(): Kuch items ke liye condition true hoti hai toh true return karta hai.
// every(): Sabhi items ke liye condition true hoti hai toh true return karta hai.
// includes(): Ek item array me hai ya nahi ye check karta hai.
// indexOf(): Ek item ka index dhundta hai array me, agar milta hai toh index return karta hai, nahi toh -1.
// push(): Array ke end me ek ya zyada items ko add karta hai.
// pop(): Array ke end se ek item ko hata deta hai aur return karta hai.
// shift(): Array ke start se ek item ko hata deta hai aur return karta hai.
// unshift(): Array ke start me ek ya zyada items ko add karta hai.
// slice(): Array ke specified portion se ek naya array banata hai.
// splice(): Array me items ko add ya remove karta hai.
// reverse(): Array ke order ko ulta kar deta hai.
// sort(): Array ke items ko sort karta hai.
// concat(): Do arrays ko ek saath join karta hai aur naya array banata hai.
// flat(): Nested arrays ko flatten karta hai.
// fill(): Array ke specified positions par ek value se fill karta hai.
// isArray(): Ek object array hai ya nahi ye check karta hai.
// toString(): Array ko string me convert karta hai.
// join(): Array ke items ko ek string me join karta hai.
// findIndex(): Ek condition ke anurup pehla matching item ka index return karta hai.
// keys(): Array ke indexes ko ek iterator object ke roop me return karta hai.
// values(): Array ke values ko ek iterator object ke roop me return karta hai.
// entries(): Array ke index-value pairs ko ek iterator object ke roop me return karta hai.
// from(): Ek array-like ya iterable object se ek naya array banata hai.
